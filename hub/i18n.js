let currentLang = "en";

export function getLang() {
  if (currentLang === "en") {
    currentLang = localStorage.getItem(LANG_KEY) || detectBrowserLang();
  }
  return currentLang;
}

function detectBrowserLang() {
  const nav = navigator.language || "";
  const code = nav.split("-")[0].toLowerCase();
  return translations[code] ? code : "en";
}

export function setLang(lang) {
  if (!translations[lang]) return;
  currentLang = lang;
  localStorage.setItem(LANG_KEY, lang);
}

/**
 * Get a translated string by key. Supports {n} and {name} placeholders.
 * Falls back to English if key is missing in current locale.
 */
export function t(key, params) {
  const dict = translations[getLang()] || translations.en;
  let str = dict[key] || translations.en[key] || key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      str = str.replace(`{${k}}`, v);
    }
  }
  return str;
}

/** Get category label by key */
export function tCat(category) {
  const map = { action: "catAction", puzzle: "catPuzzle", survival: "catSurvival", idle: "catIdle" };
  return t(map[category] || category);
}

/** Get all available languages */
export function getLanguages() {
  return LANGUAGES;
}

/** Refresh all i18n-bound elements on the page */
export function refreshPageText() {
  // Update elements with data-i18n attribute
  for (const el of document.querySelectorAll("[data-i18n]")) {
    const key = el.dataset.i18n;
    el.textContent = t(key);
  }
  // Update elements with data-i18n-placeholder
  for (const el of document.querySelectorAll("[data-i18n-placeholder]")) {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  }
  // Update elements with data-i18n-aria
  for (const el of document.querySelectorAll("[data-i18n-aria]")) {
    el.setAttribute("aria-label", t(el.dataset.i18nAria));
  }
  // Update page title
  document.title = "Arcade Hub | " + t("heroTagline");
}

